func GenerateWordlist(domains []string) []string {
	var wordlist []string

	for _, domain := range domains {
		for _, prefix := range CommonSubdomainPrefixes {
			wordlist = append(wordlist, fmt.Sprintf("%s.%s", prefix, domain))
		}
	}

	return wordlist
}
